public class LogicalOps {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        boolean andResult = a && b;
        System.out.println(andResult);
    }
}

// The definitions:

/*
AND Operator (&&):

Returns true if both operands are true.
OR Operator (||):

Returns true if at least one operand is true.
NOT Operator (!):

Negates the boolean value of its operand.
Order of Precedence:

NOT (!), AND (&&), OR (||).
Logical operators are essential for creating conditions and making decisions in Java programs.
 */