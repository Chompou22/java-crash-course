public class IfElse {
    public static void main(String[] args) {
        int number = 15;

        if (number < 10) {
            System.out.println("The number is less than 10");
        } else if (number <= 20) {
            System.out.println("The number is between 10 and 20");
        } else {
            System.out.println("The number is greater than 20");
        }
    }
}
